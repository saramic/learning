def next_gen(gen)
  [].tap do |result|
    born = 0
    gen.each do |ttl|
      if ttl == 0
        result << 6
        born += 1
      else
        result << ttl - 1
      end
    end
    result.concat([8]*born)
  end
end

this_gen = INPUTS
80.times do
  this_gen = next_gen(this_gen)
end

puts this_gen.size
