#!/usr/bin/env ruby -Ilib

class Chunk
  def self.parse(line)
    new(line.chars)
  end

  def initialize(chars)
    @chars = chars
    @first_corrupt_char_idx = nil
  end

  def corrupted?
    state == :corrupted
  end

  def first_corrupt_char
    raise NotImplementedError unless corrupted?

    @chars.fetch(@first_corrupt_char_idx)
  end

  def state
    @state ||= _state
  end

  private
    CHAR_PAIRS = {
      ">" => "<",
      ")" => "(",
      "}" => "{",
      "]" => "[",
    }

    def _state
      stack = []

      @chars.each_with_index do |ch, idx|
        case ch
        when *CHAR_PAIRS.values
          stack << ch
        when *CHAR_PAIRS.keys
          if stack.last == CHAR_PAIRS.fetch(ch)
            stack.pop
          else
            @first_corrupt_char_idx = idx
            return :corrupted
          end
        else
          raise NotImplementedError
        end
      end

      stack.empty? ? :complete : :incomplete
    end
end

SCORES = {
  ")" => 3,
  "]" => 57,
  "}" => 1197,
  ">" => 25137,
}

INPUTS = DATA.read.split("\n").map { Chunk.parse(_1.strip) }

puts(
  INPUTS
    .select(&:corrupted?)
    .map(&:first_corrupt_char)
    .map { SCORES.fetch(_1) }
    .sum
)

